# ── Public API ────────────────────────────────────────────────────────────────

def export_to_excel(data: dict, report_type: str = "full") -> bytes:
    """Build a multi-sheet Excel workbook from platform data and return raw bytes.

    Parameters
    ----------
    data : dict
        May contain any of: port_results, route_results, insights,
        freight_data, macro_data, stock_data, sentiment_data.
    report_type : str
        "full" for all sheets, or a subset key: "freight", "port",
        "stock", "macro", "sentiment", "signals".

    Returns
    -------
    bytes
        Raw .xlsx bytes suitable for st.download_button or file I/O.
    """
    _require_openpyxl()

    if data is None:
        data = {}

    wb = Workbook()
    # Remove the default empty sheet openpyxl creates
    default_sheet = wb.active
    if default_sheet is not None:
        wb.remove(default_sheet)

    include = _SUBSET_MAP.get(report_type, _SUBSET_MAP["full"])

    for sheet_name in include:
        builder = _SHEET_BUILDERS.get(sheet_name)
        if builder is None:
            continue
        ws = wb.create_sheet(title=sheet_name)
        ws.sheet_view.showGridLines = False
        ws.row_dimensions[1].height = 24
        try:
            builder(ws, data)
        except Exception as exc:
            logger.error(f"excel_export: error building sheet '{sheet_name}': {exc}")
            ws.cell(row=3, column=1, value=f"Sheet build error: {exc}")

    if not wb.sheetnames:
        # Safety: never return an empty workbook
        ws = wb.create_sheet("Summary")
        ws.cell(row=1, column=1, value="No data available.")

    return _workbook_to_bytes(wb)


def get_excel_download_button_args(data: dict, report_type: str = "full") -> dict:
    """Prepare arguments for st.download_button to offer an Excel export.

    Usage
    -----
        args = get_excel_download_button_args(data)
        st.download_button(**args)

    Parameters
    ----------
    data : dict
        Platform data dict (same schema as export_to_excel).
    report_type : str
        Workbook subset: "full", "freight", "port", "stock", "macro",
        "sentiment", or "signals".

    Returns
    -------
    dict with keys: label, data, file_name, mime
    """
    timestamp = datetime.now(tz=timezone.utc).strftime("%Y%m%d_%H%M")
    file_name = f"shiptracker_{report_type}_{timestamp}.xlsx"
    excel_bytes = export_to_excel(data, report_type=report_type)
    return {
        "label":     "Download Excel Report",
        "data":      excel_bytes,
        "file_name": file_name,
        "mime":      "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet",
    }


# ── Legacy single-sheet helpers (preserved for backward compatibility) ────────

def export_port_data(port_results: list) -> bytes:
    """Export port demand data as a single-sheet Excel workbook."""
    return export_to_excel({"port_results": port_results}, report_type="port")


def export_route_data(route_results: list, freight_data: dict) -> bytes:
    """Export route and freight rate data as a two-sheet Excel workbook."""
    return export_to_excel(
        {"route_results": route_results, "freight_data": freight_data},
        report_type="freight",
    )


def export_signal_data(signals: list) -> bytes:
    """Export alpha signals as a single-sheet Excel workbook."""
    return export_to_excel({"stock_data": signals}, report_type="signals")


def export_full_report(
    port_results,
    route_results,
    insights,
    freight_data,
    macro_data,
    stock_data,
    signals=None,
) -> bytes:
    """Export everything as a styled multi-sheet Excel workbook.

    All parameters map to the corresponding data dict keys consumed by
    export_to_excel().  The ``signals`` argument is merged into stock_data
    when provided.
    """
    effective_stock = signals or stock_data
    return export_to_excel(
        {
            "port_results":   port_results,
            "route_results":  route_results,
            "insights":       insights,
            "freight_data":   freight_data,
            "macro_data":     macro_data,
            "stock_data":     effective_stock,
        },
        report_type="full",
    )
