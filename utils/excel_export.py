"""
utils/excel_export.py
──────────────────────
Multi-sheet Excel export for the ShipTracker Intelligence Platform.

Produces institutional-quality workbooks with:
  - Navy header rows (RGB 15, 40, 90 text on white background, bold)
  - Alternating white / light-gray data rows
  - Auto-fit column widths (approximated via openpyxl)
  - Frozen top row per sheet
  - Platform footer on every sheet

Dependencies: openpyxl
"""
from __future__ import annotations

import io
from datetime import datetime, timezone
from typing import Any

from loguru import logger

try:
    import openpyxl
    from openpyxl import Workbook
    from openpyxl.styles import (
        Alignment,
        Border,
        Font,
        PatternFill,
        Side,
    )
    from openpyxl.utils import get_column_letter
    _OPENPYXL_OK = True
except ImportError:
    _OPENPYXL_OK = False
    logger.error("openpyxl not installed — Excel export unavailable")


# ── Palette ────────────────────────────────────────────────────────────────────

_NAVY           = "0F285A"      # RGB 15, 40, 90 — header text colour
_HEADER_BG      = "0F285A"      # deep navy header background
_WHITE          = "FFFFFF"
_ALT_ROW_BG     = "F0F2F6"      # soft grey alternating row
_GREEN_TEXT     = "0A6B3C"
_RED_TEXT       = "B91C1C"
_FOOTER_GREY    = "8A8A8A"

PLATFORM_FOOTER = "Generated by ShipTracker Intelligence Platform"

# Pre-built style objects (module-level singletons for performance)
_HEADER_FONT    = Font(name="Calibri", bold=True,  color=_WHITE, size=10)
_DATA_FONT      = Font(name="Calibri", bold=False, color=_NAVY,  size=10)
_TITLE_FONT     = Font(name="Calibri", bold=True,  color=_NAVY,  size=13)
_FOOTER_FONT    = Font(name="Calibri", italic=True, color=_FOOTER_GREY, size=9)

_FILL_HEADER    = PatternFill(fill_type="solid", fgColor=_HEADER_BG)
_FILL_WHITE     = PatternFill(fill_type="solid", fgColor=_WHITE)
_FILL_ALT       = PatternFill(fill_type="solid", fgColor=_ALT_ROW_BG)

_THIN_SIDE      = Side(style="thin", color="D1D5DB")
_THIN_BORDER    = Border(
    left=_THIN_SIDE, right=_THIN_SIDE,
    top=_THIN_SIDE, bottom=_THIN_SIDE,
)

_ALIGN_LEFT     = Alignment(horizontal="left",   vertical="center", wrap_text=False)
_ALIGN_CENTER   = Alignment(horizontal="center", vertical="center", wrap_text=False)
_ALIGN_RIGHT    = Alignment(horizontal="right",  vertical="center", wrap_text=False)


# ── Private style helpers ─────────────────────────────────────────────────────

def _require_openpyxl() -> None:
    if not _OPENPYXL_OK:
        raise ImportError(
            "openpyxl is required for Excel export. "
            "Install it with: pip install openpyxl"
        )


def _style_header_row(ws, row_num: int) -> None:
    """Navy background, white bold text on a header row."""
    for cell in ws[row_num]:
        cell.fill      = _FILL_HEADER
        cell.font      = _HEADER_FONT
        cell.alignment = _ALIGN_CENTER
        cell.border    = _THIN_BORDER


def _style_data_row(ws, row_num: int, alt: bool = False) -> None:
    """Alternating fill + navy data font; right-align numbers with green/red colouring."""
    fill = _FILL_ALT if alt else _FILL_WHITE
    for cell in ws[row_num]:
        cell.fill   = fill
        cell.border = _THIN_BORDER
        if isinstance(cell.value, (int, float)):
            cell.alignment = _ALIGN_RIGHT
            if cell.value > 0:
                cell.font = Font(name="Calibri", size=10, color=_GREEN_TEXT)
            elif cell.value < 0:
                cell.font = Font(name="Calibri", size=10, color=_RED_TEXT)
            else:
                cell.font = _DATA_FONT
        else:
            cell.font      = _DATA_FONT
            cell.alignment = _ALIGN_LEFT


def _auto_fit_columns(ws, min_width: int = 9, max_width: int = 50) -> None:
    """Auto-size column widths from content length (approximation)."""
    for col_cells in ws.columns:
        max_len = 0
        col_letter = get_column_letter(col_cells[0].column)
        for cell in col_cells:
            try:
                val_len = len(str(cell.value)) if cell.value is not None else 0
                if val_len > max_len:
                    max_len = val_len
            except Exception:
                pass
        ws.column_dimensions[col_letter].width = min(max(max_len * 1.1 + 2, min_width), max_width)


def _freeze_top_rows(ws, freeze_row: int = 3) -> None:
    """Freeze rows above freeze_row so header stays visible on scroll."""
    ws.freeze_panes = f"A{freeze_row}"


def _add_sheet_title(ws, title: str, ncols: int = 7) -> None:
    """Write a large styled title in row 1."""
    ws.append([title])
    r = ws.max_row
    ws.row_dimensions[r].height = 22
    cell = ws.cell(row=r, column=1)
    cell.font      = _TITLE_FONT
    cell.alignment = _ALIGN_LEFT
    if ncols > 1:
        try:
            ws.merge_cells(start_row=r, start_column=1, end_row=r, end_column=min(ncols, 20))
        except Exception:
            pass


def _write_table(ws, headers: list[str], rows: list[list[Any]]) -> int:
    """Append header row then data rows; returns the row index after the last data row."""
    ws.append(headers)
    _style_header_row(ws, ws.max_row)

    for i, row in enumerate(rows):
        ws.append(row)
        _style_data_row(ws, ws.max_row, alt=(i % 2 == 1))

    return ws.max_row


def _add_footer(ws, row_num: int, ncols: int) -> None:
    """Append platform footer spanning all columns."""
    # Ensure we're writing at exactly row_num by padding if needed
    while ws.max_row < row_num - 1:
        ws.append([])
    ws.append([PLATFORM_FOOTER])
    r = ws.max_row
    ws.row_dimensions[r].height = 14
    cell = ws.cell(row=r, column=1)
    cell.font      = _FOOTER_FONT
    cell.alignment = _ALIGN_LEFT
    if ncols > 1:
        try:
            ws.merge_cells(start_row=r, start_column=1, end_row=r, end_column=min(ncols, 20))
        except Exception:
            pass


def _placeholder_row(ws, ncols: int, msg: str = "No data available for this period.") -> None:
    ws.append([msg])
    r = ws.max_row
    cell = ws.cell(row=r, column=1)
    cell.font      = Font(name="Calibri", italic=True, color=_FOOTER_GREY, size=10)
    cell.alignment = _ALIGN_LEFT
    if ncols > 1:
        try:
            ws.merge_cells(start_row=r, start_column=1, end_row=r, end_column=min(ncols, 20))
        except Exception:
            pass


def _workbook_to_bytes(wb) -> bytes:
    buf = io.BytesIO()
    wb.save(buf)
    buf.seek(0)
    return buf.read()


