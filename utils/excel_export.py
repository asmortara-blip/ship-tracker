def _build_macro_indicators(ws, data: dict) -> None:
    headers = ["Indicator", "Current Value", "Prior Value", "Delta", "Delta %", "Direction", "Source"]
    ncols   = len(headers)
    generated = datetime.now(tz=timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    _add_sheet_title(ws, "Macro Indicators", ncols)
    ws.append([])

    macro_data = data.get("macro_data") or {}
    if isinstance(macro_data, dict):
        items = [dict(indicator=k, **v) if isinstance(v, dict) else dict(indicator=k, current=v)
                 for k, v in macro_data.items()]
    elif isinstance(macro_data, list):
        items = macro_data
    else:
        items = []

    rows: list[list[Any]] = []
    for item in items:
        if isinstance(item, dict):
            delta = item.get("delta") or item.get("change") or "—"
            try:
                direction = "▲" if float(str(delta).replace(",", "")) > 0 else "▼"
            except (ValueError, TypeError):
                direction = "—"
            rows.append([
                item.get("indicator") or item.get("name") or item.get("series_id") or "—",
                item.get("current") or item.get("value") or item.get("current_value") or "—",
                item.get("prior") or item.get("prior_value") or item.get("previous") or "—",
                delta,
                item.get("delta_pct") or item.get("change_pct") or item.get("pct_change") or "—",
                direction,
                item.get("source") or "—",
            ])
        else:
            rows.append([str(item)] + ["—"] * (ncols - 1))

    if not rows:
        _placeholder_row(ws, ncols)
        last = ws.max_row
    else:
        last = _write_table(ws, headers, rows)

    ws.append([])
    _add_footer(ws, last + 2, ncols)
    _auto_fit_columns(ws)
    _freeze_top_rows(ws, freeze_row=3)


