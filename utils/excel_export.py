def _build_summary(ws, data: dict) -> None:
    headers = ["Metric", "Value"]
    ncols   = len(headers)
    generated = datetime.now(tz=timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    _add_sheet_title(ws, "Market Summary Dashboard", ncols)
    ws.append([])

    # Extract metrics safely
    freight    = data.get("freight_data") or {}
    bdi        = (freight.get("bdi") or freight.get("BDI") or
                  freight.get("baltic_dry_index") or "N/A")

    sentiment  = data.get("sentiment_data") or []
    if isinstance(sentiment, list) and sentiment:
        scores   = [s.get("score", 0) for s in sentiment if isinstance(s, dict)]
        avg_sent = sum(scores) / len(scores) if scores else 0
        top_sent = "Bullish" if avg_sent > 0.1 else ("Bearish" if avg_sent < -0.1 else "Neutral")
    elif isinstance(sentiment, dict):
        top_sent = sentiment.get("overall", "N/A")
    else:
        top_sent = "N/A"

    port_results  = data.get("port_results")  or []
    route_results = data.get("route_results") or []
    insights      = data.get("insights")      or []
    stock_data    = data.get("stock_data")    or []
    macro_data    = data.get("macro_data")    or {}

    signals_count = len(stock_data) if isinstance(stock_data, list) else 0

    rows = [
        ["Report Generated",      generated],
        ["Baltic Dry Index (BDI)", str(bdi)],
        ["Overall Sentiment",      top_sent],
        ["Ports Monitored",        len(port_results)],
        ["Routes Monitored",       len(route_results)],
        ["Market Signals",         signals_count],
        ["Key Insights",           len(insights) if isinstance(insights, list) else "N/A"],
        ["Macro Series Tracked",   len(macro_data) if isinstance(macro_data, dict) else "N/A"],
        ["Data Freshness",         "Real-time / T+1 lag"],
        ["Platform",               "ShipTracker Intelligence v2.1"],
    ]

    last_data_row = _write_table(ws, headers, rows)
    ws.append([])
    _add_footer(ws, last_data_row + 2, ncols)
    _auto_fit_columns(ws)
    _freeze_top_rows(ws, freeze_row=3)


def _build_freight_rates(ws, data: dict) -> None:
    headers = ["Route Name", "Current Rate (USD/TEU)", "WoW Change (%)", "YoY Change (%)", "Trend", "Last Updated"]
    ncols   = len(headers)
    generated = datetime.now(tz=timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    _add_sheet_title(ws, "Freight Rate Monitor", ncols)
    ws.append([])

    freight_data = data.get("freight_data") or {}
    routes_raw   = (freight_data.get("routes") or
                    freight_data.get("route_list") or
                    data.get("route_results") or [])

    rows: list[list[Any]] = []
    for i, route in enumerate(routes_raw):
        if isinstance(route, dict):
            rows.append([
                route.get("name") or route.get("route") or route.get("route_name") or f"Route {i+1}",
                route.get("rate") or route.get("current_rate") or route.get("spot_rate") or "N/A",
                route.get("wow_change") or route.get("week_change") or route.get("rate_change_pct") or "N/A",
                route.get("yoy_change") or route.get("year_change") or "N/A",
                route.get("trend") or route.get("signal") or "—",
                route.get("updated") or route.get("timestamp") or generated,
            ])
        else:
            rows.append([str(route), "N/A", "N/A", "N/A", "—", generated])

    if not rows:
        _placeholder_row(ws, ncols)
        last = ws.max_row
    else:
        last = _write_table(ws, headers, rows)

    ws.append([])
    _add_footer(ws, last + 2, ncols)
    _auto_fit_columns(ws)
    _freeze_top_rows(ws, freeze_row=3)


